GLOBAL.methods.util.display_modal = (_option) => {
	_option = Object.assign({
		mode: null,
		complete: function () { return true; }
	}, _option);

	if (_option.mode === true) {
		$('.p-modal').show();

		_option.complete();
	}
	else {
		$('.p-modal').hide();

		_option.complete();
	}
};


